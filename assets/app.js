window.addEventListener('error', function (event) {
  const status = document.getElementById('status');
  const diag = document.getElementById('diagText');
  const message = 'Script error: ' + (event.message || 'Unknown error');
  if (status) { status.textContent = message; status.classList.remove('hidden'); }
  if (diag) diag.textContent = message + '\n' + (event.error && event.error.stack ? event.error.stack : '');
});

window.addEventListener('unhandledrejection', function (event) {
  const status = document.getElementById('status');
  const diag = document.getElementById('diagText');
  const reason = event.reason || 'Unknown promise rejection';
  const message = 'Workbook error: ' + (reason.message || reason);
  if (status) { status.textContent = message; status.classList.remove('hidden'); }
  if (diag) diag.textContent = String(reason.stack || reason);
});

const DATA_URL = './data/current-event.json';
const SOURCE_URL = 'https://live.sfrautox.com/#N';

const state = {
  data: null,
  view: 'overall',
  selectedClass: 'all',
  driverIndex: [],
  compareSelections: ['', '', '']
};

function escapeHtml(value) {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function normalizeKey(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/[^a-z0-9#]+/g, '')
    .trim();
}

function setStatus(message, isError = false) {
  const el = document.getElementById('status');
  if (!el) return;

  el.textContent = message;
  el.classList.toggle('hidden', !message);
  el.style.borderColor = isError ? 'rgba(255,62,62,0.6)' : 'rgba(223,255,0,0.26)';
  el.style.color = isError ? '#ff9a9a' : 'rgba(245,245,245,0.68)';
}

async function loadData() {
  try {
    setStatus('Loading live event data from GitHub Pages JSON...');

    const response = await fetch(`${DATA_URL}?v=${Date.now()}`, { cache: 'no-store' });
    if (!response.ok) throw new Error(`HTTP ${response.status} while loading ${DATA_URL}`);

    const liveData = await response.json();
    state.data = normalizeIncomingData(liveData);
    state.driverIndex = buildDriverIndex(state.data);
    state.view = 'overall';
    state.selectedClass = 'all';
    state.compareSelections = ['', '', ''];

    hydrateMeta();
    render();
    updateDiagnostics(null, null, 'live-json');
    setStatus('');
  } catch (error) {
    console.error(error);
    setStatus(`Could not load live JSON: ${error.message || error}. Use CHOOSE EXCEL as a fallback.`, true);
    const diag = document.getElementById('diagText');
    if (diag) {
      diag.textContent = [
        'LIVE JSON LOAD ERROR',
        String(error?.stack || error),
        '',
        `Expected file: ${DATA_URL}`,
        'On GitHub Pages, this file is generated by the included GitHub Action.',
        'When opened directly from a PC, browser file:// rules may block fetch. Use the Excel fallback.'
      ].join('\n');
    }
  }
}

