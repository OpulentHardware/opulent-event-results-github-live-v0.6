function parseWorkbookRows(rows, fileName = 'event-results.xlsx') {
  const classes = {};
  const classOrder = [];
  const flatDrivers = [];
  let currentClass = '';
  let title = String(fileName || '').replace(/\.xlsx$/i, '');
  let date = '';

  rows.forEach(row => {
    row.forEach(cell => {
      const text = cleanWorkbookCell(cell);
      if (!text) return;
      const dateMatch = text.match(/^Date:\s*(.+)$/i);
      if (dateMatch) date = dateMatch[1].trim();
      if (/Live Results/i.test(text)) title = text;
    });
  });

  rows.forEach(row => {
    const first = cleanWorkbookCell(row[0]);
    const second = cleanWorkbookCell(row[1]);
    const third = cleanWorkbookCell(row[2]);

    if (!first) return;
    if (/^(class|position)$/i.test(first)) return;
    if (/^date:/i.test(first)) return;

    const classHeader = !second && !third && !Number.isFinite(Number(first));
    if (classHeader) {
      currentClass = first.toUpperCase();
      if (!classes[currentClass]) classes[currentClass] = [];
      if (!classOrder.includes(currentClass)) classOrder.push(currentClass);
      return;
    }

    if (!currentClass || !second) return;

    const position = Number(first);
    const bestRaw = parseWorkbookTime(row[3]);
    const bestPax = parseWorkbookTime(row[4]);
    if (!Number.isFinite(position) || !Number.isFinite(bestRaw)) return;

    const split = splitDriverAndNumber(second);
    const runs = splitRuns(row[5]);

    const displayClass = currentClass === 'N' && split.declaredClass ? `N${split.declaredClass}` : (split.declaredClass || currentClass);

    const entry = {
      position,
      rank: position,
      driver: split.driver,
      number: split.number,
      car: cleanWorkbookCell(row[2]),
      cls: currentClass,
      groupClass: currentClass,
      class: split.declaredClass || currentClass,
      declaredClass: split.declaredClass || '',
      displayClass,
      classNumber: [displayClass, split.number].filter(Boolean).join(' '),
      bestRaw,
      bestPax,
      rawTime: bestRaw,
      indexedTime: bestPax,
      time: bestRaw,
      runs
    };

    classes[currentClass].push(entry);
    flatDrivers.push(entry);
  });

  Object.values(classes).forEach(group => group.sort((a, b) => a.position - b.position));

  const overall = flatDrivers
    .filter(row => Number.isFinite(row.bestRaw))
    .slice()
    .sort((a, b) => a.bestRaw - b.bestRaw)
    .map((row, index) => ({
      ...row,
      rank: index + 1,
      position: index + 1,
      overallRank: index + 1,
      time: row.bestRaw,
      rawTime: row.bestRaw
    }));

  const pax = flatDrivers
    .filter(row => Number.isFinite(row.bestPax))
    .slice()
    .sort((a, b) => a.bestPax - b.bestPax)
    .map((row, index) => ({
      ...row,
      rank: index + 1,
      position: index + 1,
      paxRank: index + 1,
      time: row.bestPax,
      indexedTime: row.bestPax
    }));

  const rankLookup = new Map();
  overall.forEach(row => rankLookup.set(driverKey(row), { overallRank: row.rank }));
  pax.forEach(row => {
    const existing = rankLookup.get(driverKey(row)) || {};
    rankLookup.set(driverKey(row), { ...existing, paxRank: row.rank });
  });

  Object.values(classes).forEach(group => {
    group.forEach(row => {
      const ranks = rankLookup.get(driverKey(row)) || {};
      row.overallRank = ranks.overallRank || null;
      row.paxRank = ranks.paxRank || null;
      row.classPosition = row.position;
    });
  });

  const cleanClassOrder = classOrder.filter(cls => classes[cls]?.length);

  return {
    status: 'ok',
    sourceUrl: SOURCE_URL,
    updatedAt: new Date().toISOString(),
    event: null,
    meta: {
      title: title || 'SFR Live Results',
      date,
      participants: flatDrivers.length,
      sourceUrl: SOURCE_URL,
      updatedAt: new Date().toISOString(),
      fileName
    },
    title: title || 'SFR Live Results',
    date,
    participants: flatDrivers.length,
    overall,
    pax,
    classes,
    classOrder: cleanClassOrder,
    diagnostics: {
      sourceRowCount: rows.length,
      overallRows: overall.length,
      paxRows: pax.length,
      classGroups: cleanClassOrder.length,
      classRows: Object.fromEntries(cleanClassOrder.map(cls => [cls, classes[cls].length]))
    }
  };
}

function cleanWorkbookCell(value) {
  if (value === null || value === undefined) return '';
  return String(value).trim();
}

function parseWorkbookTime(value) {
  if (typeof value === 'number') return value;
  const number = Number.parseFloat(cleanWorkbookCell(value).replace(/,/g, ''));
  return Number.isFinite(number) ? number : NaN;
}

function splitDriverAndNumber(value) {
  const text = cleanWorkbookCell(value);
  const classMatch = text.match(/\(([A-Za-z0-9]+)\)\s*$/);
  const declaredClass = classMatch ? classMatch[1].toUpperCase() : '';
  const withoutClass = declaredClass ? text.replace(/\s*\([A-Za-z0-9]+\)\s*$/, '').trim() : text;
  const numberMatch = withoutClass.match(/#[A-Za-z0-9]+/);
  const number = numberMatch ? numberMatch[0].trim() : '';
  const driver = number ? withoutClass.replace(number, '').replace(/\s+/g, ' ').trim() : withoutClass;
  return { driver, number, declaredClass };
}

function splitRuns(value) {
  const text = cleanWorkbookCell(value);
  if (!text) return [];
  return text.split(/\r?\n/).map(cleanWorkbookCell).filter(Boolean);
}

function driverKey(row) {
  return normalizeKey(`${row.driver}|${row.number || ''}|${row.cls || row.class || ''}`);
}

async function readXlsx(arrayBuffer) {
  const zip = parseZip(arrayBuffer);
  const workbookXml = await zip.text('xl/workbook.xml');
  const workbookRelsXml = await zip.text('xl/_rels/workbook.xml.rels');
  const workbookDoc = parseXml(workbookXml);
  const relsDoc = parseXml(workbookRelsXml);

  const firstSheet = workbookDoc.querySelector('sheet');
  if (!firstSheet) throw new Error('No worksheet found in workbook.');
  const relId = firstSheet.getAttribute('r:id');
  const rel = Array.from(relsDoc.querySelectorAll('Relationship')).find(node => node.getAttribute('Id') === relId);
  if (!rel) throw new Error('Could not resolve first worksheet relationship.');

  const target = rel.getAttribute('Target').replace(/^\//, '');
  const sheetPath = target.startsWith('xl/') ? target : `xl/${target}`;

  const sharedStrings = zip.has('xl/sharedStrings.xml')
    ? parseSharedStrings(parseXml(await zip.text('xl/sharedStrings.xml')))
    : [];
  const sheetXml = await zip.text(sheetPath);
  const rows = parseSheet(parseXml(sheetXml), sharedStrings);
  return { rows, sheetPath };
}

function parseXml(text) {
  const doc = new DOMParser().parseFromString(text, 'application/xml');
  const error = doc.querySelector('parsererror');
  if (error) throw new Error('Could not parse workbook XML.');
  return doc;
}

function parseSharedStrings(doc) {
  return Array.from(doc.querySelectorAll('si')).map(si => Array.from(si.querySelectorAll('t')).map(t => t.textContent || '').join(''));
}

function parseSheet(doc, sharedStrings) {
  const rows = [];
  for (const rowNode of doc.querySelectorAll('sheetData row')) {
    const rowIndex = Number(rowNode.getAttribute('r')) || rows.length + 1;
    const row = [];
    for (const cell of rowNode.querySelectorAll('c')) {
      const ref = cell.getAttribute('r') || '';
      const colIndex = columnIndex(ref.replace(/\d+/g, ''));
      row[colIndex] = readCell(cell, sharedStrings);
    }
    rows[rowIndex - 1] = row;
  }
  return rows.map(row => row || []);
}

function readCell(cell, sharedStrings) {
  const type = cell.getAttribute('t');
  if (type === 'inlineStr') return cell.querySelector('is t')?.textContent || '';
  const value = cell.querySelector('v')?.textContent;
  if (value === undefined || value === null) return '';
  if (type === 's') return sharedStrings[Number(value)] || '';
  if (type === 'b') return value === '1';
  const numeric = Number(value);
  return Number.isFinite(numeric) ? numeric : value;
}

function columnIndex(letters) {
  let index = 0;
  for (const char of letters) index = index * 26 + char.charCodeAt(0) - 64;
  return Math.max(0, index - 1);
}

function parseZip(arrayBuffer) {
  const bytes = new Uint8Array(arrayBuffer);
  const view = new DataView(arrayBuffer);
  const eocdOffset = findEndOfCentralDirectory(view);
  const entries = readCentralDirectory(view, bytes, eocdOffset);

  return {
    has(name) { return entries.has(name); },
    async text(name) {
      const entry = entries.get(name);
      if (!entry) throw new Error(`Missing workbook part: ${name}`);
      const data = readEntryData(view, bytes, entry);
      const inflated = await inflateEntry(data, entry.compressionMethod);
      return new TextDecoder('utf-8').decode(inflated);
    }
  };
}

function findEndOfCentralDirectory(view) {
  for (let offset = view.byteLength - 22; offset >= Math.max(0, view.byteLength - 66000); offset--) {
    if (view.getUint32(offset, true) === 0x06054b50) return offset;
  }
  throw new Error('This does not look like a valid .xlsx ZIP file.');
}

function readCentralDirectory(view, bytes, eocdOffset) {
  const entryCount = view.getUint16(eocdOffset + 10, true);
  let offset = view.getUint32(eocdOffset + 16, true);
  const entries = new Map();

  for (let i = 0; i < entryCount; i++) {
    if (view.getUint32(offset, true) !== 0x02014b50) throw new Error('Invalid ZIP central directory.');
    const compressionMethod = view.getUint16(offset + 10, true);
    const compressedSize = view.getUint32(offset + 20, true);
    const uncompressedSize = view.getUint32(offset + 24, true);
    const nameLength = view.getUint16(offset + 28, true);
    const extraLength = view.getUint16(offset + 30, true);
    const commentLength = view.getUint16(offset + 32, true);
    const localHeaderOffset = view.getUint32(offset + 42, true);
    const nameBytes = bytes.slice(offset + 46, offset + 46 + nameLength);
    const name = new TextDecoder('utf-8').decode(nameBytes);
    entries.set(name, { compressionMethod, compressedSize, uncompressedSize, localHeaderOffset });
    offset += 46 + nameLength + extraLength + commentLength;
  }
  return entries;
}

function readEntryData(view, bytes, entry) {
  const offset = entry.localHeaderOffset;
  if (view.getUint32(offset, true) !== 0x04034b50) throw new Error('Invalid ZIP local file header.');
  const nameLength = view.getUint16(offset + 26, true);
  const extraLength = view.getUint16(offset + 28, true);
  const dataStart = offset + 30 + nameLength + extraLength;
  return bytes.slice(dataStart, dataStart + entry.compressedSize);
}

async function inflateEntry(data, method) {
  if (method === 0) return data;
  if (method !== 8) throw new Error(`Unsupported ZIP compression method: ${method}`);
  if (!('DecompressionStream' in window)) {
    throw new Error('This browser cannot decompress local .xlsx files. Use current Chrome or Edge.');
  }
  const stream = new Blob([data]).stream().pipeThrough(new DecompressionStream('deflate-raw'));
  const buffer = await new Response(stream).arrayBuffer();
  return new Uint8Array(buffer);
}
